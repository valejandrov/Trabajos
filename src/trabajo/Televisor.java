
package trabajo;


public class Televisor extends Productos{
    private String marca;
    private String modelo;
    private int pulgadas;
    private String smarTv;
    private String empresaCable;

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public int getPulgadas() {
        return pulgadas;
    }

    public void setPulgadas(int pulgadas) {
        this.pulgadas = pulgadas;
    }

    public String getSmarTv() {
        return smarTv;
    }

    public void setSmarTv(String smarTv) {
        this.smarTv = smarTv;
    }

    public String getEmpresaCable() {
        return empresaCable;
    }

    public void setEmpresaCable(String empresaCable) {
        this.empresaCable = empresaCable;
    }

    
    


    

    
    
}
